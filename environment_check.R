# Retinoblastoma (RB) Project - Environment Setup and Preprocessing Scripts

This repository contains scripts used for environment setup and data preprocessing tasks supporting the toxicogenomics study of retinoblastoma (RB) progression.

## Project Context
This repository is part of a broader investigation into the role of environmental toxicants and their subsequent microRNAs in retinoblastoma research.

Other related repositories exist but are currently private until the research is formally published.

## Repository Link
- [New Environment Setup - GitHub Repository](https://github.com/AnessK-GenoTox/New-Environment-Setup)

## Contents

- `preprocessing/`
  - Scripts for cleaning, normalizing, and preparing input datasets (e.g., miRNA-seq, mRNA-seq).
- `environment_setup/`
  - Scripts for configuring R (v4.5.0) and Python environments, and installing necessary packages.
- `utilities/`
  - Helper functions for basic quality control (QC) and data management tasks.

## Technologies Used

- **R** (version 4.5.0)
  - Key packages: `DESeq2`, `ggplot2`, `pheatmap`
- **Python** (version 3.9 or higher)
  - Key packages: `pandas`, `numpy`, `seaborn`
- **Shell Scripts**
  - For automating environment and dependency setup.

## How to Use

1. Clone the repository:
    ```bash
    git clone git@github.com:AnessK-GenoTox/New-Environment-Setup.git
    ```

2. Navigate to the relevant folder (`preprocessing/`, `environment_setup/`, or `utilities/`).

3. Follow the instructions and comments inside each script for execution.

4. Before running the scripts, ensure your R and Python environments are configured using the setup scripts.

## Important Notes

- **Data Privacy**: Raw datasets are not included in this repository to maintain data confidentiality and ethical research practices.
- **Standardized Inputs**: Scripts assume pre-standard

